package classwork.lesson10;

public class Switch {
    public static void main(String[] args) {
        int a = 1;
        switch (a) {
            case 0:
                System.out.println("a = 0");
                break;
            case 1:
                System.out.println("a = 1");
                break;
            case 3:
                System.out.println("a = 3");
                break;
            default:
                System.out.println("error");
        }
    }
}
